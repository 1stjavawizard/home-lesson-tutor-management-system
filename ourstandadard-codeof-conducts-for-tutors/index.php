                                <h2 class="ffb-id-p76b6jl blog-grid-title-lg fg-text-dark"><a href="/ourstandadard-codeof-conducts-for-tutors/">Heralding &#8216;Our Standard Code of Conduct for Tutors&#8217; for efficient service delivery</a></h2>
                                <div class="ffb-id-p76bbao post-content ff-post-content-element fg-text-dark">
                                    <div class="post-content ff-richtext">
                                        <p>We do not compromise integrity and character to get results, our tutors have the both professional and character development skills to help our students for a great future.It is a requirement of membership for individual tutors that they adhere to the following Code of ethics. Corporate members also have a responsibility to promote regard for the ‘Our Standard Code of conduct for tutors’ . Our corporate members are responsible for monitoring whether tutors with whom they work honour the code.</p>
                                        <ol>
                                            <li>I understand that my role as a tutor is to encourage and enable pupils to achieve their unique potential as independent learners through acknowledgement, encouragement, understanding, and personalised attention.</li>
                                            <li>I will be careful to avoid engaging in any practice that undermines the independent learning of our students</li>
                                            <li>I will not engage in any form of plagiarism, such as completing pupils’ homework assignments for them.</li>
                                            <li>I will demonstrate faith in my pupils’ learning ability and provide honest, positive and constructive feedback.</li>
                                            <li>I understand the need to be flexible in my approach to tutoring to assist my pupils in discovering effective learning strategies.</li>
                                            <li>I undertake to keep up-to-date with advances in subject knowledge and pedagogy.</li>
                                            <li>I undertake to comply with the UN Child Protection Policy.</li>
                                            <li>I am committed to identifying any particular challenges or difficulties my pupils might have with their learning and to assisting them in overcoming those barriers.</li>
                                            <li>I will share with my home education support manager any concerns I have about pupils’ social, emotional and behavioural difficulties that are beyond my competency to address.</li>
                                            <li>I will identify to my home education support manager any special educational needs that are beyond my experience or ability to resolve, in order that they may secure for them the right kind of specialist help from the team at Nobel tutors NG.</li>
                                            <li>I understand that my relationship to my pupils is professional and not personal and that I have a duty of care towards them.</li>
                                            <li>I will keep information about the pupil whom I am assigned confidential, unless doing so would be to result in injury or harm being done to them.</li>
                                            <li>I will show respect for my pupils’ cultural background, personal dignity and values.</li>
                                            <li>I will maintain accurate records of tutoring sessions as expected and required.</li>
                                            <li>I will respect the terms and conditions of my contract, and in particular, will not seek to provide educational services to pupils I have been introduced to in this way independently of the company.</li>
                                        </ol>
